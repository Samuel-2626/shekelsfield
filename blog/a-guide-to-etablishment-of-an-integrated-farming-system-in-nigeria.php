<h1>A Guide to Establishment of an Integrated Farming System in Nigeria</h1>
    <div class="date"><i class="fa fa-calendar fa-1x" style="color: #7dce94;"></i>&nbsp;&nbsp; Aug 9, 2021</div>


      <span class="tag">Farming</span>

      
      <div>

      <br>
      <hr>

      <h2>Introduction</h2>
      <p>
      Integrated farming system (IFS) is a cross-linked farming system involving 
      high-quality production of organic crops, fish, livestock, apiary, and 
      renewable energy with environmental, economic, social and intergenerational 
      sustainability.
      </p>

      <img src="../img/blog-post-1.jpg" alt="blog-post-1" class="img-fluid img-thumbnail">
      <br>
      <br>

      <p>
      IFS is of great benefits to the farmers through the proper processing 
      and using of farm waste. This system ensures low cost of production and 
      enhances productivity. It offers healthy soil and farm produce through organic 
      production. It also guarantees an all year-round income generation for the farmers. 
      </p>

      <img src="../img/blog-post-2.jpg" alt="blog-post-2" class="img-fluid img-thumbnail">
      <br>
      <br>

      <p>
      In Nigeria, going into farming in the first instance, is a sure 
      step in the right direction as the country is traditionally an agrarian 
      nation with about two hundred million population and high deficit in food supply. 
      From the current reality of rapid reduction in crude oil export and consequent 
      reduction in exchange rate and foreign reserves, food will continue to be in 
      deficit of supply for quite a long time in the country! More so, the country 
      in endowed with a very large area of farmland that is yet to be put into resourceful 
      use. Against this background, integrated farming is a veritable option for job creation, 
      poverty alleviation and food security. 
      </p>

      <img src="../img/blog-post-3.jpg" alt="blog-post-3" class="img-fluid img-thumbnail">
      <br>
      <br>

      <p>
      This piece is therefore prepared to guide anyone 
      who may want to develop passion for integrated farming 
      in Nigeria. Our general economic knowledge of factors of
      production viz: land, capital, labour and entrepreneur will be explored. 
      This will be translated into where to establish the farm, that is the 
      location and land availability, how to raise money, management resources 
      and marketing potentials.
      </p>

      <img src="../img/blog-post-4.jpg" alt="blog-post-4" class="img-fluid img-thumbnail">
      <br>
      <br>

      <p>
      Let us now look into the various steps to take in establishing a 
      medium scale integrated farming:
      </p>

      <h2>Step 1: Identify your location and right farmland</h2>

      <p>
      Your location of farm establishment has to be informed by the 
      farm needs of people that your farm intends to supply. This 
      requires a bit of market survey. Even if you know nothing about formal 
      market research practices, you can do your own research by getting out to 
      learn more about your potential customers, distribution channels among others. 
      In addition, you need to know your competitors in that location so that you are 
      not choked up before starting. If you are already interested in a particular 
      product, learn more about your potential local market. Check out farmers' markets, 
      meet other local producers, speak to customers as you shop. Better yet, survey farmers' 
      markets to see if any farm products are under-represented. In addition, consult 
      the local extension office near your proposed location of operation for proper guidance. 
      </p>

      <img src="../img/blog-post-5.jpg" alt="blog-post-5" class="img-fluid img-thumbnail">
      <br>
      <br>

      <p>
      Once you have decided on what to produce on your farm, the next decision 
      will focus on whether to buy land or lease it. If you buy land, you will 
      have the full control over its use, but you will also assume financial risk 
      for the success of your enterprise. This is one of the major reasons leasing 
      land is a popular option for many new farmers. It minimizes financial risk and 
      requires reduced capital at the outset.
      </p>

      <p>
      If you are interested in leasing farmland, consider finding people who own land, 
      but who are not doing anything with it or you may approach the state government 
      for possible availability of farmland for lease in your choice location. Buying a 
      farm, or farmland, is not usually the best option for a starter, however, if it is 
      desired, you may carefully approach community head or religious leaders of your 
      choice location if there is availability of farmland for sale.  
      </p>

      <p>
      Before you find the right people to help you buy land, 
      it is worth familiarizing yourself with the things you will 
      need to consider. This will include among others:
      </p>

      <p>
      <strong>Proximity to markets:</strong> Consider where you're going to sell 
      your products, or how you are going to reach sales channels. If they are far 
      kilometres away, you are going to struggle much more getting to market. 
      Often, it is easiest to start local and go from there. You will likely 
      already have completed your market research by now and should have a very 
      good idea of where your market is located. 
      </p>

      <p>
      <strong>Access to water:</strong> It is very important to make sure you have a 
      steady supply of water, so be sure to ask plenty of questions and 
      consider all of your options. How will you provide water for the plants, 
      animals, and processing needs of your business?
      </p>

      <p>
      <strong>Soil quality:</strong> As with water, high-quality soil is a must for most 
      crop farmers. Ask the current owner about the nutrient status of the soil.  
      </p>

      <p>
      <strong>Facilities and Infrastructure:</strong> Depending on the type of farm you want, 
      you may also need different outbuildings. A produce stand or farm shop 
      might require an up-front investment. What about your livestock? Does the 
      land include shelters for the animals you plan to raise? What about processing 
      facilities? Different crops and animal products will require different processing 
      and storage facilities. What sort of transportation and roads are available to 
      and from the location? 
      </p>

      <img src="../img/blog-post-6.jpg" alt="blog-post-6" class="img-fluid img-thumbnail">
      <br>
      <br>


      <p>
      <strong>Neighbors:</strong> These can be a great resource, or a great hindrance, 
      depending. Do they produce farm goods? What are their production 
      practices? Are they compatible with yours? Operating a successful 
      farming operation will happen a lot more easily if you have a good 
      relationship with your neighbors.
      </p>

      <h2>Step 2: Sourcing for capital</h2>

      <p>
      Getting money to start any business is usually a very big challenge, most especially 
      to the majority of Nigerians who have poor background.  Capital could be sourced as a 
      loan from financial institutions such as agricultural banks, loan could also be sourced 
      from relatives, friends and family etc. For anyone seeking a loan, writing a business 
      plan is going to be essential. This is not any different for an aspiring farm owner. 
      Even if you are not seeking a loan, a business plan is a useful tool to help you 
      figure out which of your ideas are feasible, and to remind you of your goals. 
      Obviously, before you sit down to write your business plan, you will need to 
      determine your cost of production. You will also need to know how much your 
      rent/lease will cost you.
      </p>
        
      <h2>Step 3: Management resources</h2>

      <p>
      The next thing for consideration is labour. Getting faithful workers 
      with integrity is a big challenge these days. These workers range from 
      farm attendants up to the farm manager, either casual or permanent. 
      Most farms have been wrecked due to nefarious activities of farm workers. 
      This could be curtailed through different security monitors and devices. 
      Another thing for consideration is the farm house for keeping the workers.
      </p>

      <h2>Step 4: Marketing potentials</h2>

      <p>
      There are many different ways to market your farm products. While farmers' 
      markets are probably the most obvious example that comes to mind, there 
      are a number of other channels you can use to market and sell your products. 
      If you have enough traffic nearby, you might find that a produce stand or farm 
      shop right on your own property is a good option. You could even find a local 
      growers' cooperative that allows you to team up with other producers to sell 
      your products under a united brand. 
      </p>

      <h2>Conclusion</h2>

      <p>
      In conclusion, integrated farming could be very rewarding if these 
      steps are well followed. However, one may start according to his or her 
      financial capacity. Starting small and growing steady has been proven the best. 
      So, you are good to start!
      </p>

